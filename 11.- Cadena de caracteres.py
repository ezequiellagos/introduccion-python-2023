# Cadena de caracteres

nombre = "ezeQUIEL LAgos"

nombre = nombre.upper()
print(nombre)

nombre = nombre.lower()
print(nombre)

nombre = nombre.capitalize()
print(nombre)

# Quita los espacios en blanco al final de la cadena de texto
nombre = nombre.strip()
print(nombre)

nombre = nombre.replace("e", "")
print(nombre)