# Slices / rebanadas

nombre = 'Francisco'

print( nombre[0] )
print( nombre[3] )

# Fra
print( nombre[0:3] )
print( nombre[:3] )

# ncisco
print( nombre[3:] )

# rancis
print( nombre[1:7] )

# rni
print( nombre[1:7:2] )

# Francisco
print( nombre[::] )

# rcc
print( nombre[1::3] )

# ocsicnarF
print( nombre[::-1] )